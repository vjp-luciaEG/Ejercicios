SELECT E.NOMBRE,E.APELLIDO1,D.NOMBRE_DPTO
FROM EMPLEADOS E INNER JOIN HISTORIAL_LABORAL HL
ON E.DNI = HL.DNI_EMPLEADO
	INNER JOIN DEPARTAMENTOS D
ON HL.COD_DPTO = D.COD_DPTO;

SELECT E.NOMBRE,E.APELLIDO1,T.NOMBRE_TRAB
FROM EMPLEADOS E INNER JOIN HISTORIAL_LABORAL HL
ON E.DNI = HL.DNI_EMPLEADO
	INNER JOIN TRABAJOS T
ON HL.COD_TRABAJO = T.COD_TRABAJO;

