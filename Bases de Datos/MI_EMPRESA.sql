CREATE DATABASE MI_EMPRESA;
USE MI_EMPRESA;

CREATE TABLE CLIENTES (
CLIENTE_NO INT PRIMARY KEY,
NOMBRE VARCHAR(25),
LOCALIDAD VARCHAR(14),
VENDEDOR_NO INT,
DEBE DECIMAL(9,2),
HABER DECIMAL(9,2),
LIMITE_CREDITO DECIMAL(9,2)
);

CREATE TABLE EMPLEADOS (
EMP_NO INT PRIMARY KEY,
APELLIDO VARCHAR(8),
OFICIO VARCHAR(10),
DIRECTOR INT,
FECHA_ALTA DATE,
SALARIO DECIMAL(6,2),
COMISION DECIMAL(6,2),
DEP_NO INT
);

CREATE TABLE PEDIDOS (
PEDIDO_NO INT PRIMARY KEY,
PRODUCTO_NO INT,
CLIENTE_NO INT,
UNIDADES INT,
FECHA_PEDIDO DATE
);

CREATE TABLE PRODUCTOS (
PRODUCTO_NO INT PRIMARY KEY,
DESCRIPCION VARCHAR(30),
PRECIO_ACTUAL DECIMAL(8,2),
STOCK_DISPONIBLE INT
);

CREATE TABLE DEPARTAMENTOS (
DEP_NO INT PRIMARY KEY,
DNOMBRE VARCHAR(14),
LOCALIDAD VARCHAR(10)
);

