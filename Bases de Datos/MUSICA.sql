CREATE DATABASE IF NOT EXISTS MUSICA;
USE MUSICA;

CREATE TABLE GRUPO (
	NUMERO_GRUPO INT PRIMARY KEY,
	NOMBRE VARCHAR(50) NOT NULL,
	AÑO_CREACION DATE);

CREATE TABLE INTEGRANTES (
	NUMERO_INTEGRANTE INT NOT NULL,
	NUMERO_GRUPO INT,
	NOMBRE VARCHAR(50) NOT NULL,
	APELLIDOS VARCHAR(100),
	AÑO_NACIMIENTO DATE,
    CONSTRAINT INT_PK
		PRIMARY KEY (NUMERO_INTEGRANTE, NUMERO_GRUPO),
	CONSTRAINT GRU_FK
		FOREIGN KEY (NUMERO_GRUPO)
        REFERENCES GRUPO(NUMERO_GRUPO)
        ON UPDATE CASCADE ON DELETE CASCADE);

CREATE TABLE DISCOS (
	NUMERO_DISCO INT,
    NUMERO_GRUPO INT,
    TITULO VARCHAR(100) NOT NULL,
    AÑO_CREACION DATE,
    CONSTRAINT DISCOS_PK 
		PRIMARY KEY (NUMERO_DISCO, NUMERO_GRUPO),
	CONSTRAINT DISCOS_GRUPO_FK 
		FOREIGN KEY (NUMERO_GRUPO)
        REFERENCES GRUPO(NUMERO_GRUPO)
        ON UPDATE CASCADE ON DELETE CASCADE);
        
CREATE TABLE CANCIONES (
	NUMERO_GRUPO INT,
    NUMERO_DISCO INT,
    NUMERO_CANCION INT,
    TITULO VARCHAR(100) NOT NULL,
    DURACION TIME,
    CONSTRAINT CANCIONES_PK 
		PRIMARY KEY (NUMERO_GRUPO, NUMERO_DISCO, NUMERO_CANCION),
	CONSTRAINT CANCIONES_GRUPO_FK
		FOREIGN KEY (NUMERO_GRUPO)
        REFERENCES GRUPO(NUMERO_GRUPO)
        ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT CANCIONES_DISCOS_FK
		FOREIGN KEY (NUMERO_DISCO)
        REFERENCES DISCOS(NUMERO_DISCO)
        ON UPDATE CASCADE ON DELETE CASCADE);

